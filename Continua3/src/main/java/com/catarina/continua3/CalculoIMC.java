package com.catarina.continua3;

/**
 *
 * @author catarina
 */
public abstract class CalculoIMC {

    protected Double magreza;
    protected Double normal;
    protected Double sobrepeso;
    protected Double obesidade;
    protected String mensagem;

    public Double getMagreza() {
        return magreza;
    }

    public void setMagreza(Double magreza) {
        this.magreza = magreza;
    }

    public Double getNormal() {
        return normal;
    }

    public void setNormal(Double normal) {
        this.normal = normal;
    }

    public Double getSobrepeso() {
        return sobrepeso;
    }

    public void setSobrepeso(Double sobrepeso) {
        this.sobrepeso = sobrepeso;
    }

    public Double getObesidade() {
        return obesidade;
    }

    public void setObesidade(Double obesidade) {
        this.obesidade = obesidade;
    }

    public String getMensagem() {
        return mensagem;
    }

    public void setMensagem(String mensagem) {
        this.mensagem = mensagem;
    }
    
    
    
    
    
    
    
    
    
}


    
    
    


